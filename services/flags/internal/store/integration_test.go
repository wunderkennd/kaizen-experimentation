type testEnv struct {
	store      *store.PostgresStore
	auditStore *store.PostgresAuditStore
	pool       *pgxpool.Pool
}

func setupIntegration(t *testing.T) testEnv {
	t.Helper()

	ctx := context.Background()
	pool, err := store.NewPool(ctx)
	require.NoError(t, err)
	t.Cleanup(pool.Close)

	// Clean test data from previous runs.
	_, err = pool.Exec(ctx, `DELETE FROM flag_audit_trail`)
	require.NoError(t, err)
	_, err = pool.Exec(ctx, `DELETE FROM flag_variants`)
	require.NoError(t, err)
	_, err = pool.Exec(ctx, `DELETE FROM feature_flags`)
	require.NoError(t, err)

	return testEnv{
		store:      store.NewPostgresStore(pool),
		auditStore: store.NewPostgresAuditStore(pool),
		pool:       pool,
	}
}

func TestPostgresStore_CreateAndGetFlag(t *testing.T) {
	env := setupIntegration(t)
	ctx := context.Background()

	f := &store.Flag{
		Name:              "integration-test-flag",
		Description:       "A test flag",
		Type:              "BOOLEAN",
		DefaultValue:      "false",
		Enabled:           true,
		RolloutPercentage: 0.5,
	}

	created, err := env.store.CreateFlag(ctx, f)
	require.NoError(t, err)
	assert.NotEmpty(t, created.FlagID)
	assert.NotEmpty(t, created.Salt, "salt should be generated by database")
	assert.Equal(t, "integration-test-flag", created.Name)
	assert.Equal(t, 0.5, created.RolloutPercentage)
	assert.True(t, created.Enabled)

	// GetFlag.
	got, err := env.store.GetFlag(ctx, created.FlagID)
	require.NoError(t, err)
	assert.Equal(t, created.FlagID, got.FlagID)
	assert.Equal(t, created.Salt, got.Salt)
	assert.Equal(t, "A test flag", got.Description)
}

func TestPostgresStore_CreateFlagWithVariants(t *testing.T) {
	env := setupIntegration(t)
	ctx := context.Background()

	f := &store.Flag{
		Name:              "variant-test-flag",
		Type:              "STRING",
		DefaultValue:      "control",
		Enabled:           true,
		RolloutPercentage: 1.0,
		Variants: []store.FlagVariant{
			{Value: "control", TrafficFraction: 0.5},
			{Value: "treatment-a", TrafficFraction: 0.3},
			{Value: "treatment-b", TrafficFraction: 0.2},
		},
	}

	created, err := env.store.CreateFlag(ctx, f)
	require.NoError(t, err)
	require.Len(t, created.Variants, 3)
	assert.Equal(t, "control", created.Variants[0].Value)
	assert.Equal(t, 0.5, created.Variants[0].TrafficFraction)
	assert.Equal(t, 0, created.Variants[0].Ordinal)
	assert.Equal(t, "treatment-b", created.Variants[2].Value)
	assert.Equal(t, 2, created.Variants[2].Ordinal)

	// Variants have their own UUIDs.
	for _, v := range created.Variants {
		assert.NotEmpty(t, v.VariantID)
		assert.Equal(t, created.FlagID, v.FlagID)
	}
}


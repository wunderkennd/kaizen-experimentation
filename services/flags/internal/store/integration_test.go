func TestPostgresStore_ListFlags(t *testing.T) {
	env := setupIntegration(t)
	ctx := context.Background()

	// Create 5 flags.
	for i := 0; i < 5; i++ {
		_, err := env.store.CreateFlag(ctx, &store.Flag{
			Name:              "list-flag-" + string(rune('a'+i)),
			Type:              "BOOLEAN",
			DefaultValue:      "false",
			RolloutPercentage: float64(i) * 0.2,
		})
		require.NoError(t, err)
	}

	// List all.
	flags, nextToken, err := env.store.ListFlags(ctx, 10, "")
	require.NoError(t, err)
	assert.Len(t, flags, 5)
	assert.Empty(t, nextToken, "should have no next page")

	// Paginated: page size 2.
	page1, token1, err := env.store.ListFlags(ctx, 2, "")
	require.NoError(t, err)
	assert.Len(t, page1, 2)
	assert.NotEmpty(t, token1)

	page2, token2, err := env.store.ListFlags(ctx, 2, token1)
	require.NoError(t, err)
	assert.Len(t, page2, 2)
	assert.NotEmpty(t, token2)

	page3, token3, err := env.store.ListFlags(ctx, 2, token2)
	require.NoError(t, err)
	assert.Len(t, page3, 1)
	assert.Empty(t, token3, "last page should have no next token")
}


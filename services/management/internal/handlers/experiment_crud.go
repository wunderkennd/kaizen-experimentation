// CreateExperiment validates the request, inserts the experiment + variants + guardrails
// in a single transaction, records an audit entry, and returns the created experiment.
func (s *ExperimentService) CreateExperiment(
	ctx context.Context,
	req *connect.Request[mgmtv1.CreateExperimentRequest],
) (*connect.Response[commonv1.Experiment], error) {
	exp := req.Msg.GetExperiment()

	if err := validation.ValidateCreateExperiment(exp); err != nil {
		return nil, err
	}

	row, variantRows, guardrailRows := store.ExperimentToRow(exp)

	tx, err := s.store.BeginTx(ctx)
	if err != nil {
		return nil, internalError("begin transaction", err)
	}
	defer tx.Rollback(ctx)

	// Insert experiment (hash_salt auto-generated by DB).
	created, err := s.store.Insert(ctx, tx, row)
	if err != nil {
		return nil, wrapDBError(err, "experiment", row.Name)
	}

	// Set experiment_id on child rows.
	for i := range variantRows {
		variantRows[i].ExperimentID = created.ExperimentID
	}
	for i := range guardrailRows {
		guardrailRows[i].ExperimentID = created.ExperimentID
	}

	if err := s.store.InsertVariants(ctx, tx, variantRows); err != nil {
		return nil, wrapDBError(err, "variant", created.ExperimentID)
	}

	if len(guardrailRows) > 0 {
		if err := s.store.InsertGuardrailConfigs(ctx, tx, guardrailRows); err != nil {
			return nil, wrapDBError(err, "guardrail_config", created.ExperimentID)
		}
	}

	if err := s.audit.Insert(ctx, tx, store.AuditEntry{
		ExperimentID: created.ExperimentID,
		Action:       "create",
		ActorEmail:   actorFromContext(ctx),
		NewState:     "DRAFT",
		DetailsJSON:  json.RawMessage(`{"source":"api"}`),
	}); err != nil {
		return nil, internalError("audit create", err)
	}

	// Guardrail override audit: ALERT_ONLY is an explicit override of the
	// safe-by-default AUTO_PAUSE behavior. Record a separate audit entry so
	// this decision is clearly visible in the audit trail.
	if created.GuardrailAction == "ALERT_ONLY" {
		overrideDetails, _ := json.Marshal(map[string]string{
			"guardrail_action": "ALERT_ONLY",
			"override_reason":  "owner set at creation",
		})
		if err := s.audit.Insert(ctx, tx, store.AuditEntry{
			ExperimentID: created.ExperimentID,
			Action:       "guardrail_override",
			ActorEmail:   actorFromContext(ctx),
			NewState:     "DRAFT",
			DetailsJSON:  overrideDetails,
		}); err != nil {
			return nil, internalError("audit guardrail_override", err)
		}
		slog.Warn("guardrail override: experiment created with ALERT_ONLY",
			"id", created.ExperimentID, "actor", actorFromContext(ctx))
	}

	if err := tx.Commit(ctx); err != nil {
		return nil, internalError("commit transaction", err)
	}

	// Read back with variants and guardrails.
	expRow, variants, guardrails, err := s.store.GetByID(ctx, created.ExperimentID)
	if err != nil {
		return nil, internalError("read back experiment", err)
	}

	slog.Info("experiment created", "id", created.ExperimentID, "name", created.Name)
	return connect.NewResponse(store.RowToExperiment(expRow, variants, guardrails)), nil
}

// GetExperiment retrieves a single experiment by ID.
func (s *ExperimentService) GetExperiment(
	ctx context.Context,
	req *connect.Request[mgmtv1.GetExperimentRequest],
) (*connect.Response[commonv1.Experiment], error) {
	id := req.Msg.GetExperimentId()
	if id == "" {
		return nil, connect.NewError(connect.CodeInvalidArgument, nil)
	}

	expRow, variants, guardrails, err := s.store.GetByID(ctx, id)
	if err != nil {
		return nil, wrapDBError(err, "experiment", id)
	}

	return connect.NewResponse(store.RowToExperiment(expRow, variants, guardrails)), nil
}

// ListExperiments returns a paginated list with optional filters.
func (s *ExperimentService) ListExperiments(
	ctx context.Context,
	req *connect.Request[mgmtv1.ListExperimentsRequest],
) (*connect.Response[mgmtv1.ListExperimentsResponse], error) {
	filter := store.ListFilter{
		PageSize:   req.Msg.GetPageSize(),
		PageToken:  req.Msg.GetPageToken(),
		State:      store.StateToString(req.Msg.GetStateFilter()),
		Type:       store.TypeToString(req.Msg.GetTypeFilter()),
		OwnerEmail: req.Msg.GetOwnerEmailFilter(),
	}

	rows, nextToken, err := s.store.List(ctx, filter)
	if err != nil {
		return nil, internalError("list experiments", err)
	}

	resp := &mgmtv1.ListExperimentsResponse{
		NextPageToken: nextToken,
	}

	for _, row := range rows {
		// For list, we fetch variants/guardrails per experiment.
		variants, err := s.store.GetVariantsByExperiment(ctx, row.ExperimentID)
		if err != nil {
			return nil, internalError("list: get variants", err)
		}
		guardrails, err := s.store.GetGuardrailsByExperiment(ctx, row.ExperimentID)
		if err != nil {
			return nil, internalError("list: get guardrails", err)
		}
		resp.Experiments = append(resp.Experiments, store.RowToExperiment(row, variants, guardrails))
	}

	return connect.NewResponse(resp), nil
}

// UpdateExperiment modifies an experiment that is in DRAFT state.
func (s *ExperimentService) UpdateExperiment(
	ctx context.Context,
	req *connect.Request[mgmtv1.UpdateExperimentRequest],
) (*connect.Response[commonv1.Experiment], error) {
	exp := req.Msg.GetExperiment()

	if err := validation.ValidateUpdateExperiment(exp); err != nil {
		return nil, err
	}

	tx, err := s.store.BeginTx(ctx)
	if err != nil {
		return nil, internalError("begin transaction", err)
	}
	defer tx.Rollback(ctx)

	// Lock the experiment row.
	current, err := s.store.GetByIDForUpdate(ctx, tx, exp.GetExperimentId())
	if err != nil {
		return nil, wrapDBError(err, "experiment", exp.GetExperimentId())
	}

	if current.State != "DRAFT" {
		return nil, preconditionError("experiment must be in DRAFT state to update")
	}

	row, variantRows, guardrailRows := store.ExperimentToRow(exp)
	row.ExperimentID = current.ExperimentID

	// Update experiment row.
	updated, err := s.store.Update(ctx, tx, row)
	if err != nil {
		return nil, wrapDBError(err, "experiment", row.ExperimentID)
	}

	// Replace variants.
	if err := s.store.DeleteVariants(ctx, tx, updated.ExperimentID); err != nil {
		return nil, internalError("delete variants", err)
	}
	for i := range variantRows {
		variantRows[i].ExperimentID = updated.ExperimentID
	}
	if err := s.store.InsertVariants(ctx, tx, variantRows); err != nil {
		return nil, wrapDBError(err, "variant", updated.ExperimentID)
	}

	// Replace guardrails.
	if err := s.store.DeleteGuardrailConfigs(ctx, tx, updated.ExperimentID); err != nil {
		return nil, internalError("delete guardrails", err)
	}
	for i := range guardrailRows {
		guardrailRows[i].ExperimentID = updated.ExperimentID
	}
	if len(guardrailRows) > 0 {
		if err := s.store.InsertGuardrailConfigs(ctx, tx, guardrailRows); err != nil {
			return nil, wrapDBError(err, "guardrail_config", updated.ExperimentID)
		}
	}

	if err := s.audit.Insert(ctx, tx, store.AuditEntry{
		ExperimentID: updated.ExperimentID,
		Action:       "config_update",
		ActorEmail:   actorFromContext(ctx),
		PreviousState: "DRAFT",
		NewState:     "DRAFT",
		DetailsJSON:  json.RawMessage(`{"source":"api"}`),
	}); err != nil {
		return nil, internalError("audit update", err)
	}

	// Guardrail override audit: detect changes to guardrail_action.
	if current.GuardrailAction != updated.GuardrailAction {
		if updated.GuardrailAction == "ALERT_ONLY" {
			overrideDetails, _ := json.Marshal(map[string]string{
				"previous_action": current.GuardrailAction,
				"new_action":      "ALERT_ONLY",
				"override_reason": "owner changed via update",
			})
			if err := s.audit.Insert(ctx, tx, store.AuditEntry{
				ExperimentID:  updated.ExperimentID,
				Action:        "guardrail_override",
				ActorEmail:    actorFromContext(ctx),
				PreviousState: "DRAFT",
				NewState:      "DRAFT",
				DetailsJSON:   overrideDetails,
			}); err != nil {
				return nil, internalError("audit guardrail_override", err)
			}
			slog.Warn("guardrail override: experiment updated to ALERT_ONLY",
				"id", updated.ExperimentID, "actor", actorFromContext(ctx))
		} else if current.GuardrailAction == "ALERT_ONLY" {
			revokeDetails, _ := json.Marshal(map[string]string{
				"previous_action": "ALERT_ONLY",
				"new_action":      updated.GuardrailAction,
				"revoke_reason":   "owner reverted via update",
			})
			if err := s.audit.Insert(ctx, tx, store.AuditEntry{
				ExperimentID:  updated.ExperimentID,
				Action:        "guardrail_override_revoked",
				ActorEmail:    actorFromContext(ctx),
				PreviousState: "DRAFT",
				NewState:      "DRAFT",
				DetailsJSON:   revokeDetails,
			}); err != nil {
				return nil, internalError("audit guardrail_override_revoked", err)
			}
			slog.Info("guardrail override revoked: experiment updated to AUTO_PAUSE",
				"id", updated.ExperimentID, "actor", actorFromContext(ctx))
		}
	}

	if err := tx.Commit(ctx); err != nil {
		return nil, internalError("commit transaction", err)
	}

	// Read back.
	expRow, variants, guardrails, err := s.store.GetByID(ctx, updated.ExperimentID)
	if err != nil {
		return nil, internalError("read back experiment", err)
	}

	slog.Info("experiment updated", "id", updated.ExperimentID)
	return connect.NewResponse(store.RowToExperiment(expRow, variants, guardrails)), nil
}
